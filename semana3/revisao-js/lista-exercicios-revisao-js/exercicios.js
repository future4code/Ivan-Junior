function criaRetangulo(lado1, lado2) {
  let calcularPerimetro = 2 * (lado1 + lado2)
  let calcularArea = lado1 * lado2
  let retangulo = {
    largura: lado1,
    altura: lado2,
    perimetro: calcularPerimetro,
    area: calcularArea
  }
  return retangulo
}

